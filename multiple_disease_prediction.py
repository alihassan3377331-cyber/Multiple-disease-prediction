import os
import pandas as pd
import numpy as np
import kagglehub
path = kagglehub.dataset_download("ehababoelnaga/multiple-disease-prediction")
path_disease=os.path.join(path,"Blood_samples_dataset_balanced_2(f).csv")
path_disease1=os.path.join(path,"blood_samples_dataset_test.csv")
disease1=pd.read_csv(path_disease)
disease2=pd.read_csv(path_disease1)

"""#concatenate the two files"""

disease=pd.concat([disease1,disease2])

disease

"""#Data describtion"""

disease.head()

disease.tail()

disease.describe()

disease.info()

disease.shape

"""#Checking the null value"""

disease.isnull().sum()

"""#check duplicate value"""

disease.duplicated()

"""#convert the string into integrer"""

unique_diseases = disease['Disease'].unique()
disease_mapping = {disease_name: i for i, disease_name in enumerate(unique_diseases)}
disease['Disease'] = disease['Disease'].replace(disease_mapping)

disease.head()

"""#Seperate training and testing data"""

x=disease.drop('Disease',axis=1)
y=disease['Disease']

"""#train_test_split"""

from sklearn.model_selection import train_test_split

x_train,x_test,y_train,y_test=train_test_split(x,y,test_size=0.2,random_state=42)

x_train.shape,x_test.shape,y_train.shape,y_test.shape

"""#data view"""

import matplotlib.pyplot as plt
import seaborn as sns

sns.distplot(disease['Disease'])

sns.distplot(disease['Cholesterol'])

plt.figure(figsize=(6,6))
sns.barplot(x='Glucose',data=disease)
plt.title('glucose_map')

sns.boxplot(x='White Blood Cells',data=disease)

sns.lineplot(x='Red Blood Cells', y='Hemoglobin', data=disease)

"""#knn classfier"""

from sklearn.neighbors import KNeighborsClassifier

knn=KNeighborsClassifier(n_neighbors=1)

knn.fit(x_train,y_train)

y_pred=knn.predict(x_test)

y_pred

"""#accyracy of knn"""

from sklearn.metrics import accuracy_score

accuracy_score(y_test,y_pred)

"""#mean absolute error"""

from sklearn.metrics import mean_absolute_error

mae=mean_absolute_error(y_test,y_pred)

mae

"""#r2 secure"""

from sklearn.metrics import r2_score

r2_score(y_true=y_test,y_pred=y_pred)

"""#decission tree"""

from sklearn.tree import DecisionTreeClassifier

dtc=DecisionTreeClassifier()

dtc.fit(x_train,y_train)

y_predition=dtc.predict(x_test)

y_predition

accuracy_score(y_test,y_predition)
